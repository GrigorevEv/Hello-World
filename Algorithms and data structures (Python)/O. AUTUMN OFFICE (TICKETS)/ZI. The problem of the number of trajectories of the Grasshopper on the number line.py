# Решите задачу о количестве способов достичь точки n из точки 1, если кузнечик умеет прыгать +1, +2 и +3.
# Кузнечик стоит на позиции 0.


def grasshopper(n):
    f = [1, 1, 2] + [0] * (n - 2)
    for i in range(3, n+1):
        f[i] = f[i - 1] + f[i - 2] + f[i - 3]
    return f[n]


print(grasshopper(4))
